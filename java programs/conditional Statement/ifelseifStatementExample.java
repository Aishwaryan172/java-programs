public class ifelseifStatementExample {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;

        // if-else if ladder
        if (a == b) {
            System.out.println("a is equal to b");
        } else if (a > b) {
            System.out.println("a is greater than b");
        } else {
            System.out.println("a is less than b");
        }
    }
}