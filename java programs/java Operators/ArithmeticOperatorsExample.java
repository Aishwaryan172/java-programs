public class ArithmeticOperatorsExample {
    public static void main(String[] args) {
        int a = 15;
        int b = 7;

        // Addition operator (+)
        int sum = a + b;
        System.out.println("Sum: " + sum);

        // Subtraction operator (-)
        int difference = a - b;
        System.out.println("Difference: " + difference);

        // Multiplication operator (*)
        int product = a * b;
        System.out.println("Product: " + product);

        // Division operator (/)
        int quotient = a / b;
        System.out.println("Quotient: " + quotient);

        // Modulus operator (%)
        int remainder = a % b;
        System.out.println("Remainder: " + remainder);

        // Increment operator (++)
        int incrementA = ++a;
        System.out.println("Increment of a: " + incrementA);

        // Decrement operator (--)
        int decrementB = --b;
        System.out.println("Decrement of b: " + decrementB);
    }
}
